void
Bitfield::update() {
  // Clears the unused bits.
  if (m_size % 8)
    *(end() - 1) &= ~value_type() << (8 - m_size % 8);

  m_set = 0;

  // Some archs have bitcounting instructions, look into writing a
  // wrapper for those.
  for (iterator itr = m_data, last = end(); itr != last; ++itr)
    m_set += bit_count_256[*itr];
}

void
Bitfield::resize(size_type s) {
  m_size = s;
  m_set = 0;

  delete [] m_data;

  if (m_size == 0) {
    m_data = NULL;

  } else {
    m_data = new value_type[size_bytes()];

    std::memset(m_data, 0, size_bytes());
  }
}

